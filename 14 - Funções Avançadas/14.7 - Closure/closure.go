package main

import "fmt"

/* A função closure sabe que quando fizer um println no texto
vai estar sempre se referindo ao texto dentro dela
*/
func closure() func() {
	texto := "Dentro da função closure"

	funcao := func() {
		fmt.Println(texto)
	}

	return funcao
}

func main() {
	texto := "Dentro da função main"
	fmt.Println(texto)

	funcaoNova := closure()
	funcaoNova()
}